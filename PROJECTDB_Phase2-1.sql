CREATE TABLE RATING(--weak entity
    Id VARCHAR2(50),
    Skin_name VARCHAR2(50) NOT NULL,
    Score Number,
    CONSTRAINT pk_Rating PRIMARY KEY(Id,Skin_name));
    
CREATE TABLE USERS(
    Id VARCHAR2(50) NOT NULL,
    Password VARCHAR2(50),
    Name VARCHAR2(50),
    Email VARCHAR2(50),
