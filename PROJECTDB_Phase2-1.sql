CREATE TABLE SKIN(
    Skin_name VARCHAR2(50) NOT NULL PRIMARY KEY,
    Price Number,
    Launch_date DATE, 
    Chroma CHAR(5),
    Kind VARCHAR2(50),
    Champ_name VARCHAR2(50) NOT NULL,
    Uni_name VARCHAR2(50) NOT NULL);

CREATE TABLE CHAMPION(
    Name VARCHAR2(50) NOT NULL PRIMARY KEY,
    Title VARCHAR2(50),
    Class VARCHAR2(50),
    Region VARCHAR2(50),
    Release_Date DATE);

CREATE TABLE SKIN_UNIVERSE(
    Name VARCHAR2(50) NOT NULL PRIMARY KEY,
    Lore VARCHAR2(500)); --수정 필요할듯

CREATE TABLE SKIN_CHANGES(--weak entity --PK 수정필요?   
